#uso de operadores basicos
var = 10 + 10
var2 = 25  - 15
var3 = 100 * 10
var4 = 80 / 5

print("valores", var, var2, var3, var4)

#presendencia
div = 10 + 5 / 7
print(div)

div = (10 + 5) / 7
print(div)

operacion = var * var2 / (div + var3) * var4

print(operacion)
